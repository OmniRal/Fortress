function TowerService.SpawnNew(TowerName: string, ThisTree: Model?, OverrideCFrame: CFrame?)
    local Model, Info = SharedAssets.Towers:FindFirstChild(TowerName) :: Model, TowerInfo[TowerName]
    if not Model or not Info or (not OverrideCFrame and not ThisTree) then return end

    local PlaceHere = if ThisTree then ThisTree:GetPivot() else OverrideCFrame

    local NewModel = Model:Clone()
    NewModel:PivotTo(PlaceHere)

    return NewModel
end

function TowerService.Stop()
    if not RunHeartbeat then return end
    RunHeartbeat:Disconnect()
    RunHeartbeat = nil
end

